package petshop.model.bean;

/**
 *
 * @author muril
 */
public class Animal {
    
    private String tipo;
    private int idade;
    private float peso;

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public float getPeso() {
        return peso;
    }

    public void setPeso(float peso) {
        this.peso = peso;
    }

    @Override
    public String toString() {
        return "Animal{" + "tipo=" + tipo + ", idade=" + idade + ", peso=" + peso + '}';
    }
    
      
}


